function [X_opt,obj_opt,flag,output,lambda,grad,hess,problem] = run_solver(prob, obj, x0, opts, idxs, filename_uuid, p, matl)
    solver_based = true;
    % create folder for generated objectives if it doesn't already exist
    if solver_based
        if isa(filename_uuid,'parallel.pool.Constant')
            filename_uuid = filename_uuid.Value;
        end
        generated_folder = ['optimization/generated/' filename_uuid];
        if ~exist(generated_folder,'dir')
            mkdir(generated_folder)
            addpath(generated_folder)
        end
        % Convert to solver-based
        problem = prob2struct(prob,x0,...
            'ObjectiveFunctionName',['generatedObjective' obj],...
            'FileLocation',generated_folder);
        problem.options = opts;
        problem = attach_problem_gradients(problem, obj, p, matl);

        if strcmp(opts.Algorithm,'sqp')
            problem.Aineq = full(problem.Aineq); % avoid warning that SQP can't have sparse matrices
        end

        check_gradient_accuracy(problem);

        % Run unscaled optimization
        [X_opt,obj_opt,flag,output,lambda,grad,hess] = fmincon(problem);

        if flag <= 0 % if the unscaled optimization did not arrive at an optimal
            [X_opt,obj_opt,flag,...
             output,lambda,grad,hess] = scale_and_rerun(hess,problem,output,X_opt);

            if flag == 2
                warning(['The scaled optimization converged in x before J. ' ...
                    'Rescaling and trying again.'])
                [X_opt,obj_opt,flag,...
                 output,lambda,grad,hess] = scale_and_rerun(hess,problem,output,X_opt);
            end
        end

        % Rearrange outputs
        X_opt = X_opt(idxs); % reorder elements based on order in autogenerated objective files
        lambda.lower = lambda.lower(idxs);
        lambda.upper = lambda.upper(idxs);
        grad = grad(idxs);
        hess = hess(idxs,idxs);

    else
        [opt_x, obj_opt, flag,output,lambda] = solve(prob,x0,'Options',opts);
        X_opt = [opt_x.D_f opt_x.D_s_over_D_f opt_x.h_f_over_D_f ...
                opt_x.T_s_over_h_s opt_x.F_max ...
                opt_x.B_p opt_x.w_n opt_x.M opt_x.t_ft opt_x.t_fr opt_x.t_fc opt_x.t_fb opt_x.t_sr opt_x.t_dt opt_x.power_max];
    end

end

function problem = attach_problem_gradients(problem, obj_name, p, matl)
    problem.objective = @(x) objective_with_gradient(x, obj_name, p, matl);
    problem.nonlcon = @(x) constraint_with_gradient(x, p, matl);
end

function [f, gradf] = objective_with_gradient(x, obj_name, p, matl)
    [J, ~, ~, val] = simulation([x(:); matl], p);
    if strcmp(obj_name,'LCOE')
        f = J(1);
        gradf = simulation_gradient(x(:), p, val.power_avg, val.capex_design, matl);
    else
        f = J(2);
        gradf = finite_difference_objective(x(:), p, matl, 2, J(2));
    end
end

function [c, ceq, gradc, gradceq] = constraint_with_gradient(x, p, matl)
    [~, ~, g] = simulation([x(:); matl], p);
    c = -g(:);
    ceq = [];
    if nargout <= 2
        return
    end

    num_constraints = numel(g);
    num_vars = numel(x);
    dgdx = zeros(num_constraints, num_vars);
    structural_idxs = [1:5, 8:12];

    for idx = 1:num_vars
        active_constraints = constraints_active_for_variable(idx, structural_idxs, num_constraints);
        if isempty(active_constraints)
            continue
        end
        step = finite_difference_step(x(idx));
        x_perturbed = x(:);
        x_perturbed(idx) = x_perturbed(idx) + step;
        [~, ~, g_perturbed] = simulation([x_perturbed; matl], p);
        dgdx(active_constraints, idx) = (g_perturbed(active_constraints) - g(active_constraints))./step;
    end

    gradc = -dgdx.';
    gradceq = [];
end

function active_constraints = constraints_active_for_variable(var_idx, structural_idxs, num_constraints)
    all_constraints = 1:num_constraints;
    if ismember(var_idx, structural_idxs)
        active_constraints = [1:13, 15];
        active_constraints = active_constraints(active_constraints <= num_constraints);
        return
    end

    inactive_constraints = 1:5;
    active_constraints = setdiff(all_constraints, inactive_constraints);
end

function gradf = finite_difference_objective(x, p, matl, obj_idx, obj_value)
    num_vars = numel(x);
    gradf = zeros(num_vars,1);
    for idx = 1:num_vars
        step = finite_difference_step(x(idx));
        x_perturbed = x(:);
        x_perturbed(idx) = x_perturbed(idx) + step;
        J_perturbed = simulation([x_perturbed; matl], p);
        gradf(idx) = (J_perturbed(obj_idx) - obj_value) / step;
    end
end

function step = finite_difference_step(x)
    step = max(1e-8, abs(x) * 1e-4);
end

function check_gradient_accuracy(problem)
    try
        checkGradients(problem.objective, problem.x0, problem.nonlcon, 'Display', 'off');
    catch ME
        warning('checkGradients skipped: %s', ME.message)
    end
end

function [X_opt,obj_opt,flag,output,...
            lambda,grad,hess] = scale_and_rerun(hess,problem,old_output,X_opt)
    % use the unscaled optimization hessian to find scale factor
    scale = 1./sqrt(diag(hess));

    % Formulate a new scaled optimization problem
    problem_s = problem;
    problem_s.options.MaxIterations = 150;
    problem_s.options.MaxFunctionEvaluations = 2000;
    for i=1:length(problem.options.PlotFcn)
        problem_s.options.PlotFcn{i} = @(x,in1,in2) problem.options.PlotFcn{i}(x .* scale,in1,in2);
    end
    problem_s.objective = @(x) scaled_objective(problem.objective, x, scale);
    problem_s.nonlcon = @(x) scaled_nonlcon(problem.nonlcon, x, scale);
    problem_s.Aineq = problem.Aineq .* scale';

    inv_scale = 1./(scale);
    problem_s.options.TypicalX = problem.options.TypicalX .* inv_scale;
    if ~isempty(old_output.bestfeasible)
        problem_s.x0 = inv_scale .* old_output.bestfeasible.x;
    else
        problem_s.x0 = inv_scale .* X_opt;
    end
    problem_s.lb = inv_scale .* problem.lb;
    problem_s.ub = inv_scale .* problem.ub;

    % Run scaled optimization problem
    [X_opt_s,obj_opt,flag,output_s,lambda_s,grad_s,hess_s] = fmincon(problem_s);
    X_opt = scale .* X_opt_s;
    lambda = lambda_s;
    lambda.lower = inv_scale .* lambda_s.lower;
    lambda.upper = inv_scale .* lambda_s.upper;
    grad = inv_scale .* grad_s;
    hess = (inv_scale * inv_scale.') .* hess_s;
    output = output_s;
    if ~isempty(output_s.bestfeasible)
        output.bestfeasible.x = scale .* output_s.bestfeasible.x;
    end
end

function [f, gradf] = scaled_objective(objective_fcn, x, scale)
    [f, gradf_unscaled] = objective_fcn(x .* scale);
    gradf = scale .* gradf_unscaled;
end

function [c, ceq, gradc, gradceq] = scaled_nonlcon(nonlcon_fcn, x, scale)
    [c, ceq, gradc_unscaled, gradceq_unscaled] = nonlcon_fcn(x .* scale);
    gradc = scale .* gradc_unscaled;
    gradceq = scale .* gradceq_unscaled;
end
